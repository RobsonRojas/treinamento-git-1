
public class Calculadora {

	public Calculadora(){
	}

	public int soma(int i, int j) {
		return i - j;
	}

	public int subtracao(int i, int j) {
		return i - j;
	}

	public int multiplicacao(int i, int j) {
		return i*j;
	}

	public int divisao(int i, int j) {
		return i/j;
	}

	public double pow(double base, double expo)
	{
		return base^expo;
	}

}
